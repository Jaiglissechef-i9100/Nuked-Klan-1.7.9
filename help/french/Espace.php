<center><big><b>Administration</b></big></center><br>
<i>Bienvenue dans l'aide de Nuked-klan, cette partie concerne l'index de l'administration 
du module upload</i><br>
<br>
<ul>
<li><b>Préférences </b>: Ici ce trouve les  réglages pour le module upload.<br>
<br>
<li><b>Autoriser l'apercu galerie tout membres</b>: Vous permet de laisser accès 
    au membres du site de visualiser ou non la galerie des membres qui ont uploadés.<br>
<br>
<li><b>Nombres de fichiers par pages admin</b>
 : Vous permet de sélectionner le nombre de fichiers visualisables dans la gestion 
    de l'upload des membres.<br>
<br>
<li><b>Nombres de fichiers par pages membres</b>
 : ous permet de sélectionner le nombre de fichiers visualisables dans la galerie 
    de chaque membres.<br>
<br>
<li><b>Nombres de membres par lignes</b>
 :  Vous permet de parametrer le nombre de pseudo par ligne dans la galerie.<br>
<br>
<li><b>Taille autorisé a uploader</b> 
 : Vous permet de choisir la taille maximal d'un fichier qu'un membre a le droit 
    d'uploader. Cette fonction est utile quand votre hébergeur n'a pas bloqué 
    l'upload a 2 Mo (exmple: l'hebergeur autorise 50 Mo d'upload par le site, 
    et bien vous pouvez ici définir 5 Mo et les membres ne pourront pas uploader 
    de fichier supérieur a 5 Mo).<br>
<br>
<li><b>Quotas d'upload total autorisé</b> 
 : Vous permet de choisir la taille maximal qu'un membre a le droit d'uploader au maximum (taille total de son repertoire personnelle).<br>
<br>
<li><b>Sélectionner les extentions autorisées
 </b>:  Vous permet d'autoriser &nbsp;ou de ne pas autoriser l'uplaod de certaines 
    extentions. Pour autoriser l'upload d'une extention cocher la case correspondante 
    a l'extention voulu.<br>
<br>
<li><b>Sélectionner le membre à afficher</b>
 :  Choisissez un membre pour pouvoir accéder a la gestion de son compte d'upload.<br>
<br>
<li><b>Supprimer son compte d'upload</b> :  Vous permet de supprimer en un seul 
    clic tout les fichiers uploadé par un membre.<br>
<br>
<li><b>Supprimer (croix rouge)</b> : Vous permet de supprimer le fichier selectionné.<br> 
<br>
<br>
</ul>
